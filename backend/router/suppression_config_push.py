# -*- coding: utf-8 -*-
"""
抑制策略配置推送API - 基于SSH文件推送

功能说明:
1. IP黑名单推送: 将平台数据库中的IP地址列表推送到网关设备，写入ip_blacklist.txt文件
2. 域名黑名单推送: 将平台数据库中的域名列表推送到DNS服务器，写入domain_blacklist.txt文件
3. 丢包策略推送: 将平台数据库中的丢包策略（IP+丢包率）推送到网关设备，写入packet_loss.json文件

配置要求:
- config.py中配置IP_BLACKLIST_CONFIG、DOMAIN_BLACKLIST_CONFIG、PACKET_LOSS_CONFIG
- 每个策略可以配置不同的远程服务器SSH连接信息
"""
from fastapi import APIRouter, HTTPException
from pydantic import BaseModel
from typing import List, Dict
import json
import logging
import paramiko
from config import IP_BLACKLIST_CONFIG, DOMAIN_BLACKLIST_CONFIG, PACKET_LOSS_CONFIG
from router.suppression import get_db_connection

router = APIRouter()
logger = logging.getLogger(__name__)


class ConfigPushManager:
    """配置推送管理器 - 通过SSH推送配置到远程设备"""
    
    def __init__(self, host: str, port: int, username: str, password: str, config_path: str):
        self.host = host
        self.port = port
        self.username = username
        self.password = password
        self.config_path = config_path  # 远程设备的配置目录，如 /opt/suppression_config
        
    def _get_sftp_client(self):
        """获取SFTP客户端"""
        ssh = paramiko.SSHClient()
        ssh.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        ssh.connect(
            self.host,
            port=self.port,
            username=self.username,
            password=self.password,
            timeout=10
        )
        return ssh.open_sftp(), ssh
        
    def push_ip_blacklist(self, ip_list: List[str]) -> bool:
        """推送IP黑名单配置文件"""
        try:
            sftp, ssh = self._get_sftp_client()
            
            # 生成配置文件内容
            content = "# IP Blacklist - Auto-generated by platform\n"
            content += f"# Total IPs: {len(ip_list)}\n"
            for ip in ip_list:
                content += f"{ip}\n"
                
            # 写入远程文件
            remote_file = f"{self.config_path}/ip_blacklist.txt"
            with sftp.open(remote_file, 'w') as f:
                f.write(content)
                
            sftp.close()
            ssh.close()
            
            logger.info(f"IP黑名单已推送到 {self.host}:{remote_file}, 共{len(ip_list)}条")
            return True
            
        except Exception as e:
            logger.error(f"推送IP黑名单失败: {e}")
            return False
            
    def push_domain_blacklist(self, domain_list: List[str]) -> bool:
        """推送域名黑名单配置文件"""
        try:
            sftp, ssh = self._get_sftp_client()
            
            # 生成配置文件内容
            content = "# Domain Blacklist - Auto-generated by platform\n"
            content += f"# Total Domains: {len(domain_list)}\n"
            for domain in domain_list:
                content += f"{domain}\n"
                
            # 写入远程文件
            remote_file = f"{self.config_path}/domain_blacklist.txt"
            with sftp.open(remote_file, 'w') as f:
                f.write(content)
                
            sftp.close()
            ssh.close()
            
            logger.info(f"域名黑名单已推送到 {self.host}:{remote_file}, 共{len(domain_list)}条")
            return True
            
        except Exception as e:
            logger.error(f"推送域名黑名单失败: {e}")
            return False
            
    def push_packet_loss_policy(self, policy_dict: Dict[str, float]) -> bool:
        """推送丢包策略配置文件（JSON格式）"""
        try:
            sftp, ssh = self._get_sftp_client()
            
            # 生成JSON配置
            content = json.dumps(policy_dict, indent=2, ensure_ascii=False)
            
            # 写入远程文件
            remote_file = f"{self.config_path}/packet_loss.json"
            with sftp.open(remote_file, 'w') as f:
                f.write(content)
                
            sftp.close()
            ssh.close()
            
            logger.info(f"丢包策略已推送到 {self.host}:{remote_file}, 共{len(policy_dict)}条")
            return True
            
        except Exception as e:
            logger.error(f"推送丢包策略失败: {e}")
            return False


