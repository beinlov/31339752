@router.post("/config-push/ip-blacklist")
async def push_ip_blacklist():
    """
    推送IP黑名单到网关设备
    
    推送内容:
    - 从数据库表 ip_blacklist 读取所有IP地址
    - 生成纯文本格式文件 ip_blacklist.txt
    - 通过SSH推送到网关设备的 /opt/suppression_config/ 目录
    - 格式示例:
      192.168.1.100
      10.0.0.50
      172.16.0.10
    
    目标设备: IP_BLACKLIST_CONFIG中配置的网关设备
    """
    if ip_blacklist_manager is None:
        raise HTTPException(status_code=503, detail="IP黑名单推送服务未初始化，请检查config.py中的IP_BLACKLIST_CONFIG")
        
    conn = get_db_connection()
    try:
        cursor = conn.cursor()
        cursor.execute("SELECT ip_address FROM ip_blacklist ORDER BY created_at DESC")
        items = cursor.fetchall()
        
        ip_list = [item['ip_address'] for item in items]
        
        success = ip_blacklist_manager.push_ip_blacklist(ip_list)
        
        if success:
            return {
                "status": "success",
                "message": f"IP黑名单已推送到网关设备 {IP_BLACKLIST_CONFIG['host']}，共{len(ip_list)}条",
                "count": len(ip_list),
                "target": f"{IP_BLACKLIST_CONFIG['host']}:{IP_BLACKLIST_CONFIG['config_path']}/ip_blacklist.txt"
            }
        else:
            raise HTTPException(status_code=500, detail="推送失败")
            
    except Exception as e:
        logger.error(f"推送IP黑名单失败: {e}")
        raise HTTPException(status_code=500, detail=f"推送失败: {str(e)}")
    finally:
        conn.close()


