@router.post("/config-push/packet-loss")
async def push_packet_loss_policy():
    """
    推送丢包策略到网关设备
    
    推送内容:
    - 从数据库表 packet_loss_policy 读取所有启用的丢包策略(IP地址+丢包率)
    - 生成JSON格式文件 packet_loss.json
    - 通过SSH推送到网关设备的 /opt/suppression_config/ 目录
    - 格式示例:
      {
        "192.168.1.100": 0.3,  // 30%丢包率
        "10.0.0.50": 0.5,      // 50%丢包率
        "172.16.0.10": 0.2     // 20%丢包率
      }
    
    目标设备: PACKET_LOSS_CONFIG中配置的网关设备
    """
    if packet_loss_manager is None:
        raise HTTPException(status_code=503, detail="丢包策略推送服务未初始化，请检查config.py中的PACKET_LOSS_CONFIG")
        
    conn = get_db_connection()
    try:
        cursor = conn.cursor()
        cursor.execute("""
            SELECT ip_address, loss_rate
            FROM packet_loss_policy
            WHERE enabled = TRUE
            ORDER BY created_at DESC
        """)
        items = cursor.fetchall()
        
        # 转换为字典 {ip: loss_rate}
        policy_dict = {item['ip_address']: item['loss_rate'] for item in items}
        
        success = packet_loss_manager.push_packet_loss_policy(policy_dict)
        
        if success:
            return {
                "status": "success",
                "message": f"丢包策略已推送到网关设备 {PACKET_LOSS_CONFIG['host']}，共{len(policy_dict)}条",
                "count": len(policy_dict),
                "target": f"{PACKET_LOSS_CONFIG['host']}:{PACKET_LOSS_CONFIG['config_path']}/packet_loss.json"
            }
        else:
            raise HTTPException(status_code=500, detail="推送失败")
            
    except Exception as e:
        logger.error(f"推送丢包策略失败: {e}")
        raise HTTPException(status_code=500, detail=f"推送失败: {str(e)}")
    finally:
        conn.close()


@router.post("/config-push/all")
async def push_all_config():
    """
    一键推送所有抑制策略配置到各自的远程设备
    包括：IP黑名单(网关) + 域名黑名单(DNS) + 丢包策略(网关)
    """
    results = {}
    conn = get_db_connection()
    
    try:
        cursor = conn.cursor()
        
        # 1. 推送IP黑名单到网关设备
        if ip_blacklist_manager:
            cursor.execute("SELECT ip_address FROM ip_blacklist")
            ip_items = cursor.fetchall()
            ip_list = [item['ip_address'] for item in ip_items]
            results['ip_blacklist'] = {
                "success": ip_blacklist_manager.push_ip_blacklist(ip_list),
                "count": len(ip_list),
                "target": IP_BLACKLIST_CONFIG['host']
            }
        else:
            results['ip_blacklist'] = {
                "success": False,
                "count": 0,
                "message": "服务未启用"
            }
        
        # 2. 推送域名黑名单到DNS服务器
        if domain_blacklist_manager:
            cursor.execute("SELECT domain FROM domain_blacklist")
            domain_items = cursor.fetchall()
            domain_list = [item['domain'] for item in domain_items]
            results['domain_blacklist'] = {
                "success": domain_blacklist_manager.push_domain_blacklist(domain_list),
                "count": len(domain_list),
                "target": DOMAIN_BLACKLIST_CONFIG['host']
            }
        else:
            results['domain_blacklist'] = {
                "success": False,
                "count": 0,
                "message": "服务未启用"
            }
        
        # 3. 推送丢包策略到网关设备
        if packet_loss_manager:
            cursor.execute("SELECT ip_address, loss_rate FROM packet_loss_policy WHERE enabled = TRUE")
            policy_items = cursor.fetchall()
            policy_dict = {item['ip_address']: item['loss_rate'] for item in policy_items}
            results['packet_loss'] = {
                "success": packet_loss_manager.push_packet_loss_policy(policy_dict),
                "count": len(policy_dict),
                "target": PACKET_LOSS_CONFIG['host']
            }
        else:
            results['packet_loss'] = {
                "success": False,
                "count": 0,
                "message": "服务未启用"
            }
        
        # 检查是否全部成功
        all_success = all(r.get('success', False) for r in results.values())
        
        return {
            "status": "success" if all_success else "partial",
            "message": "配置推送完成",
            "details": results
        }
        
    except Exception as e:
        logger.error(f"推送配置失败: {e}")
        raise HTTPException(status_code=500, detail=f"推送失败: {str(e)}")
    finally:
        conn.close()


