@router.post("/config-push/deploy/{service_type}")
async def deploy_config_service(service_type: str):
    """
    一键部署配置服务（IP黑名单/域名黑名单/丢包策略）
    
    service_type: ip-blacklist, domain-blacklist, packet-loss
    
    自动执行：
    1. 创建目录结构（配置目录 + 脚本目录）
    2. 上传监控脚本
    3. 检查并安装依赖
    4. 配置sudo权限
    5. 创建systemd服务
    6. 启动监控服务
    7. 验证部署
    """
    import time
    import os
    from datetime import datetime
    
    # 选择对应的配置
    config_map = {
        "ip-blacklist": IP_BLACKLIST_CONFIG,
        "domain-blacklist": DOMAIN_BLACKLIST_CONFIG,
        "packet-loss": PACKET_LOSS_CONFIG
    }
    
    if service_type not in config_map:
        raise HTTPException(status_code=400, detail=f"不支持的服务类型: {service_type}")
    
    config = config_map[service_type]
    
    if not config.get('enabled', False):
        raise HTTPException(status_code=503, detail=f"{service_type}服务未在config.py中启用")
    
    progress_log = []
    
    def log_progress(step: str, status: str, message: str):
        progress_log.append({
            'step': step,
            'status': status,
            'message': message,
            'timestamp': datetime.now().isoformat()
        })
        logger.info(f"[部署-{service_type}] {step}: {message}")
    
    try:
        # 建立SSH连接
        log_progress("连接SSH", "running", f"正在连接到 {config['host']}:{config['port']}...")
        ssh = paramiko.SSHClient()
        ssh.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        ssh.connect(
            config['host'],
            port=config['port'],
            username=config['username'],
            password=config['password'],
            timeout=10
        )
        log_progress("连接SSH", "success", "SSH连接成功")
        
        # 步骤1: 创建目录结构
        log_progress("创建目录", "running", "正在创建目录结构...")
        commands = [
            "mkdir -p /opt/suppression_config",
            "mkdir -p /opt/suppression_scripts",
            f"chown -R {config['username']}:{config['username']} /opt/suppression_config",
            f"chown -R {config['username']}:{config['username']} /opt/suppression_scripts"
        ]
        
        for cmd in commands:
            stdin, stdout, stderr = ssh.exec_command(f"sudo {cmd}")
            exit_status = stdout.channel.recv_exit_status()
            if exit_status != 0:
                error_msg = stderr.read().decode('utf-8')
                log_progress("创建目录", "error", f"命令失败: {cmd}, 错误: {error_msg}")
                raise Exception(f"创建目录失败: {error_msg}")
        
        log_progress("创建目录", "success", "目录结构创建成功")
        
        # 步骤2: 上传监控脚本
        log_progress("上传脚本", "running", "正在上传监控脚本...")
        sftp = ssh.open_sftp()
        
        # 确定要上传的脚本文件
        script_map = {
            "ip-blacklist": "gateway_black_white_file.py",
            "domain-blacklist": "dns_black_white_file.py",
            "packet-loss": "gateway_packet_loss_file.py"
        }
        
        script_file = script_map[service_type]
        script_dir = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'suppression_scripts')
        local_script = os.path.join(script_dir, script_file)
        remote_script = f"/opt/suppression_scripts/{script_file}"
        
        if not os.path.exists(local_script):
            raise Exception(f"本地脚本不存在: {local_script}")
        
        # 读取并上传脚本（使用二进制模式避免编码问题）
        with open(local_script, 'r', encoding='utf-8') as f:
            content = f.read()
        
        content_bytes = content.encode('utf-8')
        
        with sftp.open(remote_script, 'wb') as remote_f:
            remote_f.write(content_bytes)
        
        sftp.chmod(remote_script, 0o755)
        log_progress("上传脚本", "success", f"监控脚本上传成功: {script_file}")
        
        # 创建初始配置文件
        log_progress("创建配置", "running", "正在创建初始配置文件...")
        if service_type == "ip-blacklist":
            config_file = "/opt/suppression_config/ip_blacklist.txt"
            content = "# IP Blacklist - Auto-generated by platform\n# Add one IP per line\n"
        elif service_type == "domain-blacklist":
            config_file = "/opt/suppression_config/domain_blacklist.txt"
            content = "# Domain Blacklist - Auto-generated by platform\n# Add one domain per line\n"
        else:  # packet-loss
            config_file = "/opt/suppression_config/packet_loss.json"
            content = json.dumps({}, indent=2)
        
        with sftp.open(config_file, 'w') as f:
            f.write(content)
        
        sftp.close()
        log_progress("创建配置", "success", f"配置文件创建成功: {config_file}")
        
        # 步骤3: 检查并安装依赖
        log_progress("安装依赖", "running", "正在检查并安装依赖...")
        
        if service_type == "ip-blacklist":
            # IP黑名单需要: ipset, iptables, python3-watchdog
            deps_check = [
                ("ipset --version", "ipset", "sudo apt-get update && sudo apt-get install -y ipset"),
                ("iptables --version", "iptables", "sudo apt-get install -y iptables"),
            ]
        elif service_type == "domain-blacklist":
            # 域名黑名单需要: dnsmasq, python3-watchdog
            # 先禁用systemd-resolved避免端口冲突
            log_progress("解决冲突", "running", "正在禁用systemd-resolved...")
            resolve_cmds = [
                "sudo systemctl stop systemd-resolved",
                "sudo systemctl disable systemd-resolved",
                "sudo rm -f /etc/resolv.conf",
                "echo 'nameserver 8.8.8.8' | sudo tee /etc/resolv.conf"
            ]
            for cmd in resolve_cmds:
                ssh.exec_command(cmd)
            log_progress("解决冲突", "success", "systemd-resolved已禁用")
            
            deps_check = [
                ("dnsmasq --version", "dnsmasq", "sudo apt-get update && sudo apt-get install -y dnsmasq"),
            ]
        else:  # packet-loss
            # 丢包策略需要: iptables, python3-watchdog
            deps_check = [
                ("iptables --version", "iptables", "sudo apt-get install -y iptables"),
            ]
        
        # 检查并安装系统依赖
        for check_cmd, dep_name, install_cmd in deps_check:
            stdin, stdout, stderr = ssh.exec_command(check_cmd)
            if stdout.channel.recv_exit_status() != 0:
                log_progress("安装依赖", "running", f"{dep_name}未安装，正在安装...")
                ssh.exec_command(install_cmd)
                time.sleep(2)
                log_progress("安装依赖", "success", f"{dep_name}安装成功")
            else:
                log_progress("安装依赖", "success", f"{dep_name}已安装，跳过")
        
        # 安装Python watchdog
        log_progress("安装依赖", "running", "正在安装Python watchdog...")
        stdin, stdout, stderr = ssh.exec_command("pip3 list | grep watchdog")
        if stdout.channel.recv_exit_status() != 0:
            ssh.exec_command("pip3 install watchdog --user")
            log_progress("安装依赖", "success", "watchdog安装成功")
        else:
            log_progress("安装依赖", "success", "watchdog已安装，跳过")
        
        # 步骤4: 配置sudo权限
        log_progress("配置权限", "running", "正在配置sudo免密码...")
        
        if service_type == "ip-blacklist":
            sudoers_line = f"{config['username']} ALL=(ALL) NOPASSWD: /usr/bin/python3, /usr/sbin/ipset, /usr/sbin/iptables"
            sudoers_file = "/etc/sudoers.d/ip-blacklist-monitor"
        elif service_type == "domain-blacklist":
            sudoers_line = f"{config['username']} ALL=(ALL) NOPASSWD: /usr/bin/python3, /usr/bin/systemctl"
            sudoers_file = "/etc/sudoers.d/dns-blacklist-monitor"
        else:  # packet-loss
            sudoers_line = f"{config['username']} ALL=(ALL) NOPASSWD: /usr/bin/python3, /usr/sbin/iptables"
            sudoers_file = "/etc/sudoers.d/packet-loss-monitor"
        
        ssh.exec_command(f"echo '{sudoers_line}' | sudo tee {sudoers_file}")
        ssh.exec_command(f"sudo chmod 0440 {sudoers_file}")
        log_progress("配置权限", "success", "sudo权限配置成功")
        
        # 步骤5: 创建systemd服务
        log_progress("创建服务", "running", "正在创建systemd服务...")
        
        service_map = {
            "ip-blacklist": ("ip-blacklist-monitor", "IP Blacklist Monitor Service"),
            "domain-blacklist": ("dns-blacklist-monitor", "DNS Blacklist Monitor Service"),
            "packet-loss": ("packet-loss-monitor", "Packet Loss Monitor Service")
        }
        
        service_name, service_desc = service_map[service_type]
        
        service_content = f"""[Unit]
Description={service_desc}
After=network.target

[Service]
Type=simple
User=root
WorkingDirectory=/opt/suppression_scripts
ExecStart=/usr/bin/python3 /opt/suppression_scripts/{script_file} --config-path /opt/suppression_config
Restart=always
RestartSec=10
StandardOutput=journal
StandardError=journal

[Install]
WantedBy=multi-user.target
"""
        
        stdin, stdout, stderr = ssh.exec_command(f"sudo tee /etc/systemd/system/{service_name}.service")
        stdin.write(service_content)
        stdin.channel.shutdown_write()
        stdout.channel.recv_exit_status()
        
        ssh.exec_command(f"sudo chmod 644 /etc/systemd/system/{service_name}.service")
        log_progress("创建服务", "success", "systemd服务创建成功")
        
        # 步骤6: 启动服务
        log_progress("启动服务", "running", "正在启动监控服务...")
        
        ssh.exec_command("sudo systemctl daemon-reload")
        ssh.exec_command(f"sudo systemctl stop {service_name}")
        time.sleep(1)
        ssh.exec_command(f"sudo systemctl start {service_name}")
        ssh.exec_command(f"sudo systemctl enable {service_name}")
        time.sleep(2)
        
        # 检查服务状态
        stdin, stdout, stderr = ssh.exec_command(f"sudo systemctl is-active {service_name}")
        service_status = stdout.read().decode('utf-8').strip()
        
        if service_status == 'active':
            log_progress("启动服务", "success", f"{service_desc}已启动")
        else:
            log_progress("启动服务", "warning", f"服务状态: {service_status}")
        
        # 步骤7: 验证部署
        log_progress("验证部署", "running", "正在验证部署...")
        time.sleep(1)
        
        stdin, stdout, stderr = ssh.exec_command("ls -la /opt/suppression_scripts/")
        script_output = stdout.read().decode('utf-8')
        
        if script_file in script_output:
            log_progress("验证部署", "success", "脚本文件验证通过")
        else:
            log_progress("验证部署", "warning", "脚本文件可能不完整")
        
        ssh.close()
        
        log_progress("部署完成", "success", f"✅ {service_type}服务部署成功！服务器: {config['host']}")
        
        return {
            'status': 'success',
            'message': f'{service_type}服务部署成功',
            'host': config['host'],
            'config_path': config['config_path'],
            'service_name': service_name,
            'service_status': service_status,
            'logs': progress_log
        }
        
    except paramiko.AuthenticationException:
        log_progress("连接SSH", "error", "SSH认证失败，请检查用户名和密码")
        return {
            'status': 'error',
            'message': 'SSH认证失败',
            'logs': progress_log
        }
    except paramiko.SSHException as e:
        log_progress("SSH错误", "error", f"SSH连接错误: {str(e)}")
        return {
            'status': 'error',
            'message': f'SSH错误: {str(e)}',
            'logs': progress_log
        }
    except Exception as e:
        log_progress("未知错误", "error", f"部署失败: {str(e)}")
        logger.exception(f"部署{service_type}服务失败")
        return {
            'status': 'error',
            'message': str(e),
            'logs': progress_log
        }


@router.get("/config-push/status/{service_type}")
async def get_config_service_status(service_type: str):
    """获取配置服务的部署和运行状态"""
    config_map = {
        "ip-blacklist": (IP_BLACKLIST_CONFIG, ip_blacklist_manager),
        "domain-blacklist": (DOMAIN_BLACKLIST_CONFIG, domain_blacklist_manager),
        "packet-loss": (PACKET_LOSS_CONFIG, packet_loss_manager)
    }
    
    if service_type not in config_map:
        raise HTTPException(status_code=400, detail=f"不支持的服务类型: {service_type}")
    
    config, manager = config_map[service_type]
    
    if not config.get('enabled', False):
        return {
            'status': 'error',
            'message': '服务未启用'
        }
    
    if not manager:
        return {
            'status': 'error',
            'message': '管理器未初始化'
        }
    
    try:
        ssh = paramiko.SSHClient()
        ssh.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        ssh.connect(
            config['host'],
            port=config['port'],
            username=config['username'],
            password=config['password'],
            timeout=5
        )
        
        # 检查目录是否存在
        stdin, stdout, stderr = ssh.exec_command(f"test -d {config['config_path']} && echo 'exists' || echo 'not_exists'")
        dir_exists = stdout.read().decode('utf-8').strip() == 'exists'
        
        # 检查配置文件
        file_map = {
            "ip-blacklist": "ip_blacklist.txt",
            "domain-blacklist": "domain_blacklist.txt",
            "packet-loss": "packet_loss.json"
        }
        config_file = file_map.get(service_type, "")
        
        stdin, stdout, stderr = ssh.exec_command(f"test -f {config['config_path']}/{config_file} && echo 'exists' || echo 'not_exists'")
        file_exists = stdout.read().decode('utf-8').strip() == 'exists'
        
        ssh.close()
        
        return {
            'status': 'success',
            'data': {
                'is_deployed': dir_exists and file_exists,
                'is_configured': config.get('enabled', False),
                'host': config['host'],
                'config_path': config['config_path'],
                'config_file': config_file if file_exists else None
            }
        }
    except Exception as e:
        return {
            'status': 'error',
            'message': str(e)
        }
